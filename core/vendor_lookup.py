class VendorLookup:
    """
    Looks up hardware manufacturers offline based on the OUI prefix of MAC addresses.
    """

    @staticmethod
    def lookup(mac_address: str) -> str:
        """
        Extracts the first 3 octets (OUI) from a MAC address string
        and resolves the manufacturer from the internal dictionary.
        Falls back to detecting randomized/private MACs.
        """
        if not mac_address or not isinstance(mac_address, str):
            logger.debug(f"VendorLookup: invalid input '{mac_address}'")
            return "Unknown"

        cleaned = mac_address.strip().upper().replace("-", ":")
        if len(cleaned) < 8:
            logger.debug(f"VendorLookup: cleaned string too short '{cleaned}'")
            return "Unknown"

        oui = cleaned[:8]

        vendor = OUI_DATABASE.get(oui)
        if vendor:
            logger.debug(f"VendorLookup: MAC OUI {oui} → '{vendor}'")
            return vendor

        else:
            try:
                first_byte = int(cleaned[:2], 16)
                if first_byte & 0x02:
                    logger.debug(f"VendorLookup: MAC OUI {oui} is randomized/private")
                    return "Private/Randomized"
            except ValueError:
                pass

        logger.debug(f"VendorLookup: MAC OUI {oui} not found in {len(OUI_DATABASE)} entries")
        logger.info(f"Unknown OUI: {oui} (MAC: {mac_address})")
        return "Unknown"
